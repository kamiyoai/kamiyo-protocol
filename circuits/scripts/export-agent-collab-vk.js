/**
 * Export Agent Collaboration verification keys for Solana
 *
 * Converts snarkjs verification keys to groth16-solana format.
 */

const fs = require("fs");
const path = require("path");

const BUILD_DIR = path.join(__dirname, "../build/mitama");
const OUTPUT_PATH = path.join(__dirname, "../../programs/mitama/src/vk_generated.rs");

const CIRCUITS = [
  {
    name: "agent_identity",
    constName: "AGENT_IDENTITY_VK",
    publicInputs: ["agents_root", "nullifier", "epoch"],
  },
  {
    name: "private_signal",
    constName: "PRIVATE_SIGNAL_VK",
    publicInputs: ["signal_commitment", "min_stake", "min_confidence", "agent_nullifier"],
  },
  {
    name: "swarm_vote",
    constName: "SWARM_VOTE_VK",
    publicInputs: ["agents_root", "action_hash", "vote_nullifier", "vote_commitment"],
  },
];

function toBigEndianBytes(value, length = 32) {
  let bn;
  if (typeof value === "string") {
    bn = BigInt(value);
  } else {
    bn = BigInt(value);
  }

  const hex = bn.toString(16).padStart(length * 2, "0");
  const bytes = [];
  for (let i = 0; i < hex.length; i += 2) {
    bytes.push(parseInt(hex.substr(i, 2), 16));
  }
  return bytes;
}

function formatRustBytes(bytes) {
  return `[${bytes.join(", ")}]`;
}

function g1ToSolana(point) {
  const x = toBigEndianBytes(point[0], 32);
  const y = toBigEndianBytes(point[1], 32);
  return [...x, ...y];
}

function g2ToSolana(point) {
  const x_c0 = toBigEndianBytes(point[0][0], 32);
  const x_c1 = toBigEndianBytes(point[0][1], 32);
  const y_c0 = toBigEndianBytes(point[1][0], 32);
  const y_c1 = toBigEndianBytes(point[1][1], 32);
  return [...x_c1, ...x_c0, ...y_c1, ...y_c0];
}

function generateVkConst(circuit) {
  const vkPath = path.join(BUILD_DIR, `${circuit.name}_vk.json`);

  if (!fs.existsSync(vkPath)) {
    console.error(`Verification key not found: ${vkPath}`);
    console.error(`Run 'bash scripts/setup-agent-collab.sh' first.`);
    process.exit(1);
  }

  const vk = JSON.parse(fs.readFileSync(vkPath, "utf8"));

  const alpha_g1 = g1ToSolana(vk.vk_alpha_1);
  const beta_g2 = g2ToSolana(vk.vk_beta_2);
  const gamma_g2 = g2ToSolana(vk.vk_gamma_2);
  const delta_g2 = g2ToSolana(vk.vk_delta_2);
  const ic = vk.IC.map((point) => g1ToSolana(point));

  const icArrayName = `${circuit.constName}_IC`;

  return {
    icConst: `const ${icArrayName}: [[u8; 64]; ${ic.length}] = [
${ic.map((p) => `    ${formatRustBytes(p)}`).join(",\n")}
];`,
    vkConst: `/// ${circuit.name} circuit verification key
///
/// Public inputs: ${circuit.publicInputs.join(", ")}
pub const ${circuit.constName}: Groth16Verifyingkey = Groth16Verifyingkey {
    nr_pubinputs: ${ic.length - 1},
    vk_alpha_g1: ${formatRustBytes(alpha_g1)},
    vk_beta_g2: ${formatRustBytes(beta_g2)},
    vk_gamme_g2: ${formatRustBytes(gamma_g2)},
    vk_delta_g2: ${formatRustBytes(delta_g2)},
    vk_ic: &${icArrayName},
};`,
  };
}

function main() {
  console.log("Exporting Agent Collaboration verification keys for Solana...\n");

  const icConsts = [];
  const vkConsts = [];

  for (const circuit of CIRCUITS) {
    console.log(`Processing ${circuit.name}...`);
    const { icConst, vkConst } = generateVkConst(circuit);
    icConsts.push(icConst);
    vkConsts.push(vkConst);
  }

  const rustCode = `//! Auto-generated Groth16 verification keys for Agent Collaboration
//!
//! Generated by: circuits/scripts/export-agent-collab-vk.js
//! DO NOT EDIT MANUALLY

use groth16_solana::groth16::Groth16Verifyingkey;

${icConsts.join("\n\n")}

${vkConsts.join("\n\n")}
`;

  fs.writeFileSync(OUTPUT_PATH, rustCode);
  console.log(`\nGenerated: ${OUTPUT_PATH}`);
}

main();
